#include"lcd12864.h"

/**
 * main.c
 */
void Clock_Init()
{
  unsigned char i;
  BCSCTL1&=~XT2OFF;                 //打开XT2振荡器
  BCSCTL2|=SELM1+SELS;              //MCLK为8MHZ，SMCLK为8MHZ
  do{
    IFG1&=~OFIFG;                   //清楚振荡器错误标志
    for(i=0;i<100;i++)
       _NOP();
  }
  while((IFG1&OFIFG)!=0);           //如果标志位1，则继续循环等待
  IFG1&=~OFIFG;
}

int main(void)
{

	WDTCTL = WDTPW | WDTHOLD;	// stop watchdog timer
	Clock_Init();
	char a[2]="曹";

	lcd12864_init();

	menu_01();

	return 0;
}
